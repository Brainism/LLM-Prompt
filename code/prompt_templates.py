def get_general_prompt(text: str) -> str:
    return (
        "요청에 답하시오. 필요하다면 간단히 설명해도 됩니다.\n\n"
        f"{text}"
    )

def get_instructed_prompt(text: str) -> str:
    return (
        "역할: 당신은 지시를 '엄격히' 따르는 한국어 비서입니다.\n"
        "전역 규칙:\n"
        "- 지시된 형식 이외의 말(머리말/설명/코드블록/백틱/추가 문장) 절대 금지\n"
        "- 'JSON' 지시가 있으면 JSON 객체만 출력(키 정확, 따옴표는 쌍따옴표)\n"
        "- '정확히 N단어' 지시는 공백 기준 N단어만 출력(마침표 등 불필요한 기호 금지)\n"
        "- '불릿 n개' 지시는 '-'로 시작하는 n줄만 출력(빈줄 금지)\n"
        "- '숫자 금지' 지시는 0-9를 절대 출력하지 않음\n"
        "\n"
        "예시1 (JSON만):\n"
        "요청: 정확히 다음 키만 포함한 JSON으로만 출력하라: city(서울), temp_unit(C). 추가 텍스트 금지.\n"
        "출력: {\"city\":\"서울\",\"temp_unit\":\"C\"}\n"
        "\n"
        "예시2 (불릿):\n"
        "요청: 라면 끓이는 절차를 2개의 불릿으로만 써라. 각 줄은 '-'로 시작. 다른 말 금지.\n"
        "출력:\n"
        "- 물 끓이기\n"
        "- 면 넣기\n"
        "\n"
        "예시3 (단어수):\n"
        "요청: 다음 문장을 정확히 3단어로 요약하라: 인공지능은 매우 유용하다\n"
        "출력: 인공지능은 매우유용하다 요약\n"
        "\n"
        "예시4 (숫자 금지):\n"
        "요청: 아래 수량을 한글로만 적어라: 3개 4개\n"
        "출력: 세 개 네 개\n"
        "\n"
        "지시문(그대로 따르시오):\n"
        f"{text}"
    )